void start_screen() {
	byte button = 0;
	render_dark(st_dd);
	render_light(st_ll);
	render();
	while(1) {
		button = CheckButtons();
		if(button==SP_EXE) {
			break;
		}
	}
}

static byte bomb_here(word x, word y) {
	if(get_tile(x,y) == 9) {
		return 1;
	}
	return 0;
}

static word get_sum(word x, word y) {
	return bomb_here(x-1,y-1) + bomb_here(x,y-1) + bomb_here(x+1,y-1) + bomb_here(x+1,y) + bomb_here(x+1,y+1) + bomb_here(x,y+1) + bomb_here(x-1,y+1) + bomb_here(x-1,y);
}

void start_minesweeper(void) {
	word i = 0;
	word j = 0;
	word k = 0;
	word z = 0;
	word cus_x = 0;
	word px = -1;
	word cus_y = 0;
	word py = -1;
	byte bomb = 0;
	byte first_button = 1;
	byte button = 0;
	word bx = 0;
	word by = 0;
	byte b_count = 0;
	byte f_count = 0;
	start_screen();

	for(i=0;i<24;i++) {
		for(j=0;j<8;j++) {
			if(get_random(get_random(10))==1) {
				bomb = 9;
				b_count++;
			} else {
				bomb = 11;
			}
			set_tile(bomb,i,j);
			draw_tile(10,i,j);
		}
	}
	for(i=0;i<24;i++) {
		for(j=0;j<8;j++) {
			if(get_sum(i,j)==0||get_tile(i,j)==9) {
				//set_tile(10,i,j);
				continue;
			}
			set_tile(get_sum(i,j)-1,i,j);
			//draw_tile(get_sum(i,j)-1,i,j);
		}
	}
	while(1) {
		button = CheckButtons();
		if(button==0xFF) {
			continue;
		}
		if(button==SP_DOWN) {
			cus_y++;
		} else if(button==SP_LEFT) {
			cus_x--;
		} else if(button==SP_RIGHT) {
			cus_x++;
		} else if(button==SP_UP) {
			cus_y--;
		} else if(button==0x0B) {
			if(get_tile(cus_x,cus_y)==8) {
				draw_tile(10,cus_x,cus_y);
			} else {
				draw_tile(8,cus_x,cus_y);
			}
			set_tile2(cus_x,cus_y);
		} else if(button==SP_EXE) {
			if(get_tile(cus_x,cus_y)==9) {
				lose();
			}
			draw_tile(get_tile(cus_x,cus_y),cus_x,cus_y);
			f_count++;
			if(get_tile(cus_x,cus_y)==11) {
				reveal_tiles(cus_x,cus_y);
			}
			set_tile2(cus_x,cus_y);
			first_button = 0;
			continue;
			//custom_break();
		}
		draw_tile2(px,py);
		if(cus_y==-1) {
			cus_y = 7;
		}
		if(cus_x==-1) {
			cus_x = 23;
		}
		if(cus_y==8) {
			cus_y = 0;
		}
		if(cus_x==24) {
			cus_x = 0;
		}
		py = cus_y;
		px = cus_x;
		set_tile2(cus_x,cus_y);
		draw_tile3(12,cus_x,cus_y);
		//draw_tile2(px,py);
	}
}
