static void draw_map()
{
	word i = 0;
	word j = 0;
	for (i=0;i<2016;i++)
	{
		if ((i & 0x1F) == 0x18) i += 0x08;
		deref(0xf037) = 0;
		deref(0xf820 + i) = lightm[j];
		deref(0xf820 + i) |= deref(0x9000 + i);
		deref(0xf037) = 4;
		deref(0xf820 + i) = darkm[j];
		deref(0xf820 + i) |= deref(0x9800 + i);
		j++;
	}
}

static byte coin()
{
	//val(0x9000) ^= 1;
	return val(0xF00D) & 1;
	//return (~((val(0xF023) & 1) - (val(0xF00D)&1)) + val(0xf022) + val(0xf023))&1;
}

static void draw_char(byte id, byte x, byte y)
{
    Write2RealScreen = 0;
	word i = 0;
	word a = (0x9000+(x>>3)+(((y-1) << 4) + ((y-1) << 3)));
	word b = (0x9000+(x>>3)+(((y+8) << 4) + ((y+8) << 3)));
	deref(a) = 0;
	deref(a+0x600) = 0;
	deref(b) = 0;
	deref(b+0x600) = 0;
	deref(a+1) = 0;
	deref(a+0x601) = 0;
	deref(b+1) = 0;
	deref(b+0x601) = 0;
	for (i=0;i<8;i++)
	{
		tui_draw_byte(x,y+i,light[((word)id<<3)+i],dark[((word)id<<3)+i],0xFF);
	}
	draw_map();
}

static void lose();

static byte get_pixel(byte x, byte y)
{
	y++;
	if (x > 191 || y > 63 || y == 0)
	{
		while(1)
		lose();
	}
    return(val(((word)y<<5)+(x>>3)+0xF800)&(0x80>>(x&7)))?1:0;
}

static void win()
{
	tui_draw_text(5,1,"You Win!!!",TUI_FONT_SIZE_8x12,0,0,0,TUI_COLOUR_BLACK);
}
static void lose()
{
	tui_draw_text(5,1,"You Lose!!!",TUI_FONT_SIZE_8x12,0,0,0,TUI_COLOUR_BLACK);
}

void start_ball(void) {
	word i = 0;
	byte key = 0;
	byte cx,cy,dx,dy,cchar;
	byte up,down,left,right;
	//(0xf09C) = 0x01;
	//deref(0xf09C) = 0x01;
    Write2RealScreen = 0;
	tui_clear_screen();
	Write2RealScreen = 1;
    tui_clear_screen();
	cx = 91;
	cy = 27;
	dx = 0;
	dy = 0;
	cchar = 0;
    Write2RealScreen = 0;
    draw_char(cchar,cx,cy);
    Write2RealScreen = 1;
	while (1)
	{
		if (!dx && !dy)
		{
			key = CheckButtons();
			if (key == 0xff)
			{
				continue;
			}
			switch(key)
			{
				case SP_UP:
					dx = 0;
					dy = -1;
					break;
				case SP_DOWN:
					dx = 0;
					dy = 1;
					break;
				case SP_LEFT:
					dx = -1;
					dy = 0;
					break;
				case SP_RIGHT:
					dx = 1;
					dy = 0;
					break;
				case SP_EXE:
					deref(0xF09C) ^= 1;
			}
		}
		left = tui_get_pixel(cx-1,cy);
		right = tui_get_pixel(cx+8,cy);
		up = tui_get_pixel(cx,cy-1);
		down = tui_get_pixel(cx,cy+8);
		if (left && dx == 0xff) {
			dx = 0;
			cchar = 4;
		} else if (right && dx == 1) {
			dx = 0;
			cchar = 6;
		} else if (up && dy == 0xff) {
			dy = 0;
			cchar = 3;
		} else if (down && dy == 1) {
			dy = 0;
			cchar = 5;
		} else {
			cx += dx;
			cy += dy;
			if (dx)
			{
				cchar = 2;
			}
			if (dy)
			{
				cchar = 1;
			}
			/*if (dx == 1)
			{
				plot_line(cx,cy+1,cx,cy+6,0);
			} else if (dy == 1) {
				plot_line(cx+1,cy,cx+6,cy,0);
			} else if (dy == 0xff) {
				plot_line(cx+1,cy+7,cx+6,cy+7,0);
			}*/
		}

		//delay(10);
		draw_char(cchar,cx,cy);
		if(cchar > 2 && cchar < 7)
		{
			cchar = 0;
			delay_ms(25);
			draw_char(cchar,cx,cy);
			if(cx == 75 && cy == 52)
			{
				delay_ms(500);
				draw_char(7,cx,cy);
				while (1)
				win();
			}
		}
	}
}
